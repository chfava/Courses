package com.example.frlafg.tp1;

public class Position {
    public int x;
    public int y;

    public Position(){
        x = 0;
        y = 0;
    }

    public Position(int xValue, int yValue){
        x = xValue;
        y = yValue;
    }

    public void setPosition(int xValue, int yValue){
        x = xValue;
        y = yValue;
    }
}
